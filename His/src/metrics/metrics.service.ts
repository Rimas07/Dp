/**
 * 📊 METRICS SERVICE - ПРОСТАЯ ВЕРСИЯ
 *
 * Это сервис для сбора метрик (статистики) работы приложения.
 *
 * ═══════════════════════════════════════════════════════════════════
 * ЧТО ТАКОЕ МЕТРИКИ ПРОСТЫМИ СЛОВАМИ:
 * ═══════════════════════════════════════════════════════════════════
 *
 * Представьте магазин:
 * - Вам нужно знать сколько покупателей зашло → МЕТРИКА: посетителей = 150
 * - Сколько что-то купили → МЕТРИКА: покупок = 75
 *
 * В вашем приложении:
 * - Сколько запросов обработано → МЕТРИКА: requests_total = 542
 * - Сколько успешных → МЕТРИКА: successful = 500
 * - Сколько ошибок → МЕТРИКА: errors = 42
 *
 * ═══════════════════════════════════════════════════════════════════
 * КАК ЭТО РАБОТАЕТ:
 * ═══════════════════════════════════════════════════════════════════
 *
 * 1. Создаём счётчик (Counter) - это просто переменная, которая только растёт
 * 2. Когда обрабатываем запрос → увеличиваем счётчик на +1
 * 3. Prometheus заходит на наш endpoint /metrics и читает все счётчики
 * 4. Prometheus сохраняет историю изменений
 * 5. Grafana берёт данные из Prometheus и рисует графики
 *
 */

import { Injectable } from '@nestjs/common';
import { Counter, Registry } from 'prom-client';

@Injectable()
export class MetricsService {
    // ═══════════════════════════════════════════════════════════════════
    // REGISTRY - "КОЛЛЕКЦИЯ" ВСЕХ МЕТРИК
    // ═══════════════════════════════════════════════════════════════════
    //
    // Представьте это как ПАПКУ, где хранятся все счётчики.
    // Когда Prometheus приходит за метриками, мы отдаём всю папку.
    //
    private readonly registry: Registry;

    // ═══════════════════════════════════════════════════════════════════
    // COUNTER - СЧЁТЧИК ЗАПРОСОВ
    // ═══════════════════════════════════════════════════════════════════
    //
    // Counter = счётчик, который только РАСТЁТ (никогда не уменьшается)
    //
    // Как работает:
    // - Начальное значение: 0
    // - Обработали запрос → +1 (теперь 1)
    // - Обработали ещё один → +1 (теперь 2)
    // - Обработали ещё один → +1 (теперь 3)
    // - И так далее...
    //
    // Prometheus будет забирать эти значения каждые 15 секунд:
    // 15:00:00 → requests_total = 100
    // 15:00:15 → requests_total = 105  (обработали 5 запросов за 15 сек)
    // 15:00:30 → requests_total = 112  (обработали 7 запросов за 15 сек)
    //
    public readonly requestsTotal: Counter;

    constructor() {
        console.log('📊 [MetricsService] Инициализация метрик...');

        // ───────────────────────────────────────────────────────────────
        // Создаём Registry - "папку" для хранения всех метрик
        // ───────────────────────────────────────────────────────────────
        this.registry = new Registry();

        // ───────────────────────────────────────────────────────────────
        // Создаём счётчик для запросов
        // ───────────────────────────────────────────────────────────────
        this.requestsTotal = new Counter({
            // name - имя метрики (будет видно в Prometheus/Grafana)
            name: 'proxy_requests_total',

            // help - описание метрики (для людей)
            help: 'Total number of proxy requests',

            // registers - в какую "папку" положить этот счётчик
            registers: [this.registry]
        });

        console.log('✅ [MetricsService] Метрики инициализированы');
        console.log('📊 [MetricsService] Создан счётчик: proxy_requests_total');
    }

    /**
     * ═══════════════════════════════════════════════════════════════════
     * ПОЛУЧИТЬ ВСЕ МЕТРИКИ В ФОРМАТЕ PROMETHEUS
     * ═══════════════════════════════════════════════════════════════════
     *
     * Эта функция вызывается когда Prometheus заходит на /metrics
     *
     * Возвращает ТЕКСТ в специальном формате:
     *
     * # HELP proxy_requests_total Total number of proxy requests
     * # TYPE proxy_requests_total counter
     * proxy_requests_total 542
     *
     * Строка 1-2: Описание метрики (для людей)
     * Строка 3: Имя метрики и её значение
     *
     */
    async getMetrics(): Promise<string> {
        return this.registry.metrics();
    }

    /**
     * ═══════════════════════════════════════════════════════════════════
     * УВЕЛИЧИТЬ СЧЁТЧИК ЗАПРОСОВ (ДЛЯ ПРИМЕРА)
     * ═══════════════════════════════════════════════════════════════════
     *
     * Эта функция будет вызываться из кода прокси:
     *
     * metricsService.incrementRequests(); // +1 к счётчику
     *
     */
    incrementRequests(): void {
        this.requestsTotal.inc(); // inc = increment = увеличить на 1
        console.log('📈 [MetricsService] Счётчик увеличен: proxy_requests_total');
    }

    /**
     * Получить текущее значение счётчика (для отладки)
     */
    async getCurrentValue(): Promise<number> {
        const metrics = await this.registry.getSingleMetric('proxy_requests_total');
        return (metrics as any).hashMap?.[''].value || 0;
    }
}
